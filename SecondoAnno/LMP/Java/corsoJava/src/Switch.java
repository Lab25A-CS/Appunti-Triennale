public class Switch {
    public static void main(String[] args){

        String nome = "Maria";

        switch(nome){
            case "Luca":
                System.out.println("ciao luca");
                break;
            case "Marco":
                System.out.println("ciao marco");
                break;
            case "Maria":
                System.out.println("ciao maria");
                break;
            case "Gennaro":
                System.out.println("ciao gennaro");
                break;
            default:
                System.out.println("ciao chiunque tu sia");
        }


    }
}

